# if & else문 예시1
# 예제_1_if_else문을_활용하여_n이_5인지_여부에_따라_다른_코드를_실행해봅시다.

n = int(input("숫자를 입력하세요"))
if n == 5:
    print("n은 5 입니다!")  # n이 5라는 조건이 참일 때 실행됩니다. 
else:
    print("n은 5가 아닙니다!")  # n이 5라는 조건이 거짓일 때 실행됩니다. 

# if & else문 예시2
#예제2_숫자를_입력_받고_100보다_크다_작다_같다를_구분해봅시다

num = input("숫자를 입력하세요")
num = int(num) # 입력받은 값을 정수형으로 변환

if num >= 100: # num이 100 이상인지 확인
    if num > 100: # num이 100보다 큰지 확인
        print("100보다 크네요")
    else: # num이 100과 같은 경우
        print("100이네요")
else: # num이 100보다 작은 경우
    print("100보다 작네요")

