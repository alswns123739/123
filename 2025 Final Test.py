#1차: 조건문의 제어구조
# 제어구조는: 순차구조, 선택구조, 반복구조

# 순차구조: 프로그램이 작성된 순서대로 실행되는 구조
# 선택구조: 조건에 따라 실행되는 구조
# 반복구조: 특정 조건이 만족될 때까지 반복되는 구조

#조건문이란?
#조건에 따라 다르게 동작을 살행하도록 작성된 코드= 조건이 참인경우 or 거짓인 경우에 따라 실행되는 코드
#비교연산자: ==, !=, <, >, <=, >=를 통해 참 또는 거짓을 판단

#if 문
# if 조건= 참이면 if문 내부코드 실행
# if 조건= 거짓이면 if문 내부코드 실행 안함

#주의: 조건 다음에는 무조건 콜론(:)이 필요
# if문 내부코드는 들여쓰기를 통해 구분

# if문 예시1
#예제1_if문을_활용하여_n이_5인지_여부에_따라_다른_코드_실행해봅시다.
n = int(input("숫자를 입력하세요"))  #int = 정수형으로 변환하는 함수
if n == 5:
    print("n은 5 입니다!")  # n이 5인 것이 참일 때 실행됩니다. 
                         
if n != 5:
    print("n은 5가 아닙니다!")  # n이 5가 아닌 것이 참일 때 실행됩니다. 

# if문 예시2
#예제2_비교_연산자를_활용하여_간단한_조건문을_만들어봅시다.
#사용자로부터 입력 받은 값에 20을 더한 값을 출력해봅시다. 단, 그 더한 값이 255를 초과할 경우 255를 출력합시다.
n = int(input())
res = n+20 #res는 변수 n= 입력값

if res>255:
    res = 255
print(res) #변수 res의 값을 출력

# if문 예시3
#정수 하나를 입력 받은 후, 해당 정수의 절댓값을 출력하는 프로그램을 작성해 봅시다.
n = int(input())

if n<0:
    n = -n

print(n)

# if문 예시4
#영단어 하나가 입력되었을 때, 그 단어가 love이면 'I love you.'를 출력하는 프로그램을 작성해 봅시다.
