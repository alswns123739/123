#3개의 정수(integer) (a, b, c)를 입력받아 짝(even)/홀(odd)을 판별하여 출력하는 프로그램을 작성해봅시다. (짝수는 even, 홀수는 odd를 출력 해야합니다.)
a, b, c = map(int, input().split()) ## map() 함수를 사용하여 입력받은 값을 정수형으로 변환하고, a, b, c에 각각 할당합니다.

if a%2 == 0: # a를 2로 나눈 나머지가 0인지 확인
    print("even")
else: # a를 2로 나눈 나머지가 0이 아닌 경우
    print("odd")

if b%2 == 0: # b를 2로 나눈 나머지가 0인지 확인
    print("even")
else:   # b를 2로 나눈 나머지가 0이 아닌 경우
    print("odd")

if c%2 == 0: # c를 2로 나눈 나머지가 0인지 확인
    print("even")
else: # c를 2로 나눈 나머지가 0이 아닌 경우
    print("odd")

